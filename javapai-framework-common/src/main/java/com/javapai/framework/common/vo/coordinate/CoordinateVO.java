package com.javapai.framework.common.vo.coordinate;

public class CoordinateVO {
	/**
	 * X坐标。
	 */
	private long x;
	/**
	 * Y坐标。
	 */
	private long y;

	public long getX() {
		return x;
	}

	public void setX(long x) {
		this.x = x;
	}

	public long getY() {
		return y;
	}

	public void setY(long y) {
		this.y = y;
	}

}
